#include <bits/stdc++.h>
// using namespace std;はプログラムを短く書くための機能
// 通常はstd::coutやstd::endlのようにstd::をはじめに付ける必要がある
using namespace std;

int main() {
  // cout しーあうと
  // 文字列を扱う際は "" で囲む
  // endl は改行を表す
  cout << "Hello, world!" << endl;
  // "Hello, world!" と endl というデータを、<< で cout に送っていくイメージ
  cout << "初めてのC++" << endl;

  // 行の一番最後には;（セミコロン）が必要

  // 数値の出力
  cout << 2020 << endl;
}
